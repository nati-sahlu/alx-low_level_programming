#include "main.h"

/**
 * mul - multiplies two integers
 * @a: one of the integers to be multiplied
 * @b: the other integer to be multiplied
 *
 * Description: This function takes two integers as arguments and returns
 * their product.
 *
 * Return: the product of a and b
 */
int mul(int a, int b)
{
	int product;

	product = a * b;
	return (product);
}

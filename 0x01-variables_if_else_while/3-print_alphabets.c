#include <stdio.h>
/**
 *  main - Entry point
 *
 *  Return: Always 0 (success)
 *  */

/* betty style doc for function main goes there */
int main(void)
{
	char c;
	char d;
        
	c = 'a';
	d = 'A';
	while 
		(c <= 'z') {
			putchar(c);
			c++;
		}
	while 
		(d <= 'z') {
			putchar (d);
			d++;
		}
	putchar('\n');
	return (0);
}
